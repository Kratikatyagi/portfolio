export default function Layout() {
  const { theme, toggle } = useTheme()
  const location = useLocation()
  const mainRef = useRef(null)

  useEffect(() => {
    if (mainRef.current) mainRef.current.scrollTop = 0
  }, [location.pathname])

  return (
    <div className={styles.viewport}>
      <div className={styles.frame}>

        {/* ── Desktop sidebar ── */}
        <nav className={styles.sidebar}>
          <div className={styles.navGroup}>
            {navItems.map(({ to, label, icon: Icon, end }) => (
              <NavLink
                key={to}
                to={to}
                end={end}
                className={({ isActive }) =>
                  [styles.navItem, isActive ? styles.active : ''].join(' ')
                }
              >
                {({ isActive }) => (
                  <>
                    <span className={styles.navIcon}><Icon active={isActive} /></span>
                    <span className={styles.navLabel}>{label}</span>
                  </>
                )}
              </NavLink>
            ))}
          </div>
          <div className={styles.themeGroup}>
            <div className={styles.divider} />
            <button onClick={toggle} className={styles.navItem} title="Toggle theme">
              <span className={styles.navIcon}>
                {theme === 'light' ? <MoonIcon /> : <SunIcon />}
              </span>
              <span className={styles.navLabel}>Theme</span>
            </button>
          </div>
        </nav>

        {/* ── Scrollable content ── */}
        <main ref={mainRef} className={styles.content}>
          <Outlet />
        </main>

        {/* ── Mobile bottom nav (hidden on desktop) ── */}
        <nav className={styles.mobileNav}>
          {navItems.map(({ to, label, icon: Icon, end }) => (
            <NavLink
              key={to}
              to={to}
              end={end}
              className={({ isActive }) =>
                [styles.mobileNavItem, isActive ? styles.active : ''].join(' ')
              }
            >
              {({ isActive }) => (
                <>
                  <span className={styles.navIcon}><Icon active={isActive} /></span>
                  <span className={styles.mobileNavLabel}>{label}</span>
                </>
              )}
            </NavLink>
          ))}
          <button onClick={toggle} className={styles.mobileNavItem} title="Toggle theme">
            <span className={styles.navIcon}>
              {theme === 'light' ? <MoonIcon /> : <SunIcon />}
            </span>
            <span className={styles.mobileNavLabel}>Theme</span>
          </button>
        </nav>

      </div>
    </div>
  )
}

